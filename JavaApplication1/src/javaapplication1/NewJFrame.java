    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int[] fib = new int[40];
             fib[0] = 0;
             fib[1] = 1;
             for (int i = 2; i < 40; i++){
                 fib[i] = fib[i-1]+fib[i-2];
             }
        String txt = jTextField1.getText();
        int p = 0;
        int txt_int = Integer.parseInt (txt.trim ());
        String str = "";
        for (int b = 0; b < 40; b++){
            if (txt_int == fib[b]) {
                str = str + fib[b] + ".";
                
                break;    
            } else {
                str = str + fib[b] + " ";
                p = p+1;
            }}
            
            if (p == 40){
                jTextField2.setText("Число не принадлежит ряду Фибоначчи");
            } else {
                jTextField2.setText(str);
            }       
    }//GEN-LAST:event_jButton1ActionPerformed
